    private void muestra() {
        for(int i = 0; i < horario.length; i++){
            String t = etiquetasTexto[horario[i]].getText();
            etiquetasTexto[horario[i]].setText(t + materias.elementAt(i)+ "   ");
            cont++;
        }
        for(int i= 0;i < etiquetasTexto.length;i++){
            String t = etiquetasTexto[i].getText();
            if(t==""){
                etiquetasTexto[i].setVisible(false);
                etiquetasNom[i].setVisible(false);
                
            }
        }
    }
       
