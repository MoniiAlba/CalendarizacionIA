    private void btnAgregaMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregaMActionPerformed
        if(listaAct.contains(listaM.getElementAt(jListaMat.getSelectedIndex()))){
            labelAdver.setText("La materia ya está dentro de la relación");
            labelAdver.setVisible(true);
        }else{
            labelAdver.setVisible(false);
            jListaRel.setVisible(true);
            listaAct.addElement(listaM.getElementAt(jListaMat.getSelectedIndex()));
            jListaRel.setModel(listaAct);
            btnAgregaRel.setEnabled(true);
            btnAgregaM.setEnabled(false);
        }        
    }//GEN-LAST:event_btnAgregaMActionPerformed

