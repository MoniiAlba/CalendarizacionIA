    public void imprimeMats(){
        for(int i = 0; i < listaM.size(); i++){
            System.out.print(listaM.getElementAt(i).toString()+" ");
        }
        System.out.println();
    }
    
    public void imprimeMatRels(){
        for(int i = 0; i < listaM.size(); i++){
            for(int j = 0; j < listaM.size(); j++){
                System.out.print(matAdy[i][j]+" ");
            }
            System.out.println();
        }
    }
    
    
    public void insertaRel(DefaultListModel l, int tam){
        for(int i = 0; i < tam; i ++){
            int sig = listaM.indexOf(listaAct.elementAt(i));
            for(int j = 0; j < tam; j++){
                if(i != j){
                    int nuevo = listaM.indexOf(listaAct.elementAt(j));                    
                    if(matAdy [sig][nuevo] == 0){
                        matAdy [sig][sig]++;
                    }
                    matAdy [sig][nuevo] = 1;
                    
                }
            }
        }
    }

    private void btnAgregaRelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregaRelActionPerformed
        String rel = "";
        int tam = listaAct.size();
        if(tam < 2){
            labelAdver.setText("Las relaciones deben de ser entre 2 materias o más.");
            labelAdver.setVisible(true);
        }else{
            insertaRel(listaAct, tam);
            imprimeMatRels();
            System.out.println();
            for(int i = 0; i < tam; i ++){
                rel+=listaAct.getElementAt(i);
                if(i < tam - 1){
                    rel+=" - ";
                }
            }
            listaRel.addElement(rel);
            listaAct.clear();
            jRelaciones.setModel(listaRel);
            btnQuitaTodas.setEnabled(true);
            btnAgregaM.setEnabled(false);
            btnQuitaM.setEnabled(false);
            btnAgregaRel.setEnabled(false);
            jListaRel.setVisible(false);
            jRelaciones.setVisible(true);
        }
    }//GEN-LAST:event_btnAgregaRelActionPerformed

